function productVariant({ id, size, price, unit = 'даана', packageInfo = 'даана', stockStatus = 'in_stock', sku }) {
  return {
    id,
    size,
    price,
    unit,
    packageInfo,
    stockStatus,
    sku,
  }
}

function getAggregateStockStatus(variants, fallbackStatus) {
  if (!variants.length) return fallbackStatus || 'in_stock'
  if (variants.some((variant) => variant.stockStatus === 'in_stock')) return 'in_stock'
  if (variants.some((variant) => variant.stockStatus === 'low_stock')) return 'low_stock'
  if (variants.some((variant) => variant.stockStatus === 'pre_order')) return 'pre_order'
  return 'out_of_stock'
}

function product(data) {
  const badges = data.badges || []
  const catalogPath = data.catalogPath || inferCatalogPath(data)
  const imageAssets = data.imageAssets || getProductAssetEntry(data.slug)
  const variants = Array.isArray(data.variants)
    ? data.variants.map((variant) =>
        productVariant({
          ...variant,
          price: Number(variant.price || 0),
          unit: variant.unit || data.unit,
          stockStatus: variant.stockStatus || data.stockStatus || 'in_stock',
        }),
      )
    : []
  const variantPrices = variants.map((variant) => Number(variant.price)).filter((price) => Number.isFinite(price) && price >= 0)
  const price = variantPrices.length ? Math.min(...variantPrices) : Number(data.price || 0)
  const stockStatus = getAggregateStockStatus(variants, data.stockStatus)

  return {
    currency: 'KGS',
    oldPrice: null,
    relatedProductIds: [],
    ...data,
    catalogPath,
    imageAssets,
    variants,
    price,
    stockStatus,
    images: data.images || imageFor(data.titleKg, data.slug),
    name: data.titleKg,
    description: data.descriptionKg,
    shortDescription: data.shortDescriptionKg,
    stock: stockStatus === 'pre_order' ? 'Заказ менен' : stockStatus === 'low_stock' ? 'Аз калды' : stockStatus === 'out_of_stock' ? 'Жок' : 'Кампада бар',
    isSale: badges.includes('sale'),
    isPopular: badges.includes('hit'),
    tags: data.tags || [...new Set([...badges, ...catalogPath])],
  }
}

