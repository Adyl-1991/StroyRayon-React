function preserveInitialCase(match, replacement) {
  const first = match.trim()[0]
  return first && first === first.toLocaleUpperCase('ky')
    ? `${replacement[0].toLocaleUpperCase('ky')}${replacement.slice(1)}`
    : replacement
}

export function normalizeKyrgyzText(value) {
  if (typeof value !== 'string') return value

  return KYRGYZ_TEXT_REPLACEMENTS.reduce(
    (text, [pattern, replacement]) => text.replace(pattern, (match) => preserveInitialCase(match, replacement)),
    value,
  )
    .replace(/\s+([,.;:!?])/gu, '$1')
    .replace(/ {2,}/gu, ' ')
    .trim()
}

export function normalizeKyrgyzContent(value) {
  if (typeof value === 'string') return normalizeKyrgyzText(value)
  if (Array.isArray(value)) return value.map(normalizeKyrgyzContent)
  if (value && typeof value === 'object') {
    return Object.fromEntries(
      Object.entries(value).map(([key, nestedValue]) => [key, normalizeKyrgyzContent(nestedValue)]),
    )
  }
  return value
}

